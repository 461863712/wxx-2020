  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    var id=options.id;
    var that = this;
    wx.request({
      url: 'http://localhost:3001/movie/detail?id='+id,
      method: "GET",
      header: {
        'content-type': 'application/json' // 默认值
      },
      success(res) {
        that.setData({
          movie: res.data
        })
        wx.setNavigationBarTitle({
          title: res.data.title+"详情"
        })
      }
    })

  },

